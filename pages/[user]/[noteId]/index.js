import Head from 'next/head'
import styles from '@/styles/Note.module.css'
import { useState } from 'react';

//Creates the new Outer File
const AddOuterFile = () => {
    const [fileData, setFileData] = useState({
        title: '',
        titleStyle: '',
        saveButton: ''
    });

    const handleSubmit = (event) => {
        event.preventDefault();
        setFileData(() => ({ 
            titleStyle: styles.titleStyle,
            saveButton: styles.saveButton  
        }));
    }

    const handleTitleChange = event => {
        event.preventDefault();
        setFileData(() => ({
            title: event.target.value
        }));
    }


    return (
        <div className='mt-3'> 

        <form onSubmit={handleSubmit}>
            <label>
                <input 
                className={fileData.titleStyle}
                type="text" 
                value={fileData.title}
                onChange={handleTitleChange}
                placeholder='Title'
                SpellCheck='false'
                />
            </label>
            <button type='submit' className={fileData.saveButton}>Save</button>
        </form>

        <div className='border border-primary rounded-4 p-5'></div>
        </div>
    );
}

//Manages all Outer Files and positions
const OuterContainer = () => {
    const [style, setStyle] = useState(styles.noBox);
    const [allOuterContainers, setAllOuterContainers] = useState([]);
    const [atLeastOneOuterFileExists, setAtLeastOneOuterFileExists] = useState(false);

    const handleRemoval = fileId => {
        const newFiles = allOuterContainers.filter(obj => obj.id !== fileId);
        setAllOuterContainers(oldFiles => [...newFiles]);
    }
    
    const addOuterFile = (event) => {
        event.preventDefault();

        const objectLength = Object.keys(allOuterContainers).length;
        const newObj = {
            id: objectLength,
            file: <AddOuterFile />
        };
        setAllOuterContainers(oldArray => [...oldArray, newObj]);
        setStyle(styles.box);
        setAtLeastOneOuterFileExists(true);
    }

    return (
        <>
            <Head>
                <title>Flow</title>
                <meta name="description" content="Generated by create next app" />
                <meta name="viewport" content="width=device-width, initial-scale=1" />
                <link rel="icon" href="/favicon.ico" />
            </Head>
            <main className={styles.main} >
                <h1>Note Title</h1>
            
                <div className='row border border-primary p-3'>
                    <button className={`btn btn-outline-primary col-4 ${style}`} onClick={addOuterFile}>Add Outer File</button>
                    <span className='col-4'></span>
                    <button className='col-4'>Add Text File</button>
                </div>
                
                <div className='px-5'>
                    {atLeastOneOuterFileExists && 
                    allOuterContainers.map(files => 
                        <div key={files.id}>
                            {files.file}
                            <button className={`btn btn-outline-primary float-end ${styles.removeOuterFile}`} onClick={() => handleRemoval(files.id)}>Remove File</button>
                        </div>)}
                </div>
    
            </main>
        </>
    );
}

export default OuterContainer;